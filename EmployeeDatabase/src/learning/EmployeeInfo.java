    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        System.out.println("Entered Button Saved");
        String name_validate = tf_Name.getText();
        String age_validate = tf_Age.getText();
        String bg_validate = tf_BloodGroup.getText();
        String cn_validate = tf_ContactNo.getText();

        java.util.Date date_validate = jDateChooser_DOJ.getDate();
        String address_validate = ta_Address.getText();

        if (name_validate == null || name_validate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for NAME");
        } else if (gender == null || gender.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Select Gender : MALE or FEMALE");
        } else if (age_validate == null || age_validate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for AGE");
        } else if (bg_validate == null || bg_validate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for BLOOD GROUP");
        } else if (cn_validate == null || cn_validate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for CONTACT NUMBER");
        } else if (cb_Qualification.getSelectedItem() == null || cb_Qualification.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for QUALIFICATION");
        } else if (date_validate == null || date_validate.toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Select DATE OF JOINING");
        } else if (address_validate == null || address_validate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter value for ADDRESS");
        } else if (employeeImage == null) {
            JOptionPane.showMessageDialog(null, "Please upload IMAGE");
        } else {
            try {
                conn = mysql.open(mysql.connectionString, mysql.userName, mysql.passWord);
                String sql = "INSERT INTO employeeinfo (Name, Gender, Age, BloodGroup, ContactNo, Qualification, DOJ, Address, EmpImage) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                stmt = conn.prepareStatement(sql);
                stmt.setString(1, tf_Name.getText());
                stmt.setString(2, gender);
                stmt.setString(3, tf_Age.getText());
                stmt.setString(4, tf_BloodGroup.getText());
                stmt.setString(5, tf_ContactNo.getText());
                stmt.setString(6, (String) cb_Qualification.getSelectedItem());
                java.util.Date date = jDateChooser_DOJ.getDate();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String strDOJ = sdf.format(date);
                stmt.setString(7, strDOJ);
                stmt.setString(8, ta_Address.getText());
                stmt.setBytes(9, employeeImage);
                stmt.execute();

                JOptionPane.showMessageDialog(null, "Data saved Succesfully");
                updateTable();
                clearFields();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Data save failed.\n" + ex.getMessage());
            } finally {
                try {
                    stmt.close();
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(EmployeeInfo.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

