    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        String empID = (jTable1.getModel().getValueAt(row, 0).toString());
        MySQLConnect mysql = new MySQLConnect();
        String sql = "Select EmployeeID, Name, Gender, Age, BloodGroup, ContactNo, Qualification, DOJ, Address, EmpImage from employeeinfo where EmployeeID='" + empID + "'";
        try (Connection conn = mysql.open(mysql.connectionString, mysql.userName, mysql.passWord);
                PreparedStatement stmt = conn.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();) {

            if (rs.next()) {

                tf_EmployeeID.setText(rs.getString("EmployeeID"));
                tf_Name.setText(rs.getString("Name"));
                String strGender = rs.getString("Gender");
                if (strGender.equals("Male")) {
                    jRadioButton_Male.setSelected(true);
                } else if (strGender.equals("Female")) {
                    jRadioButton_Female.setSelected(true);
                }
                tf_Age.setText(Integer.toString(rs.getInt("Age")));
                tf_BloodGroup.setText(rs.getString("BloodGroup"));
                tf_ContactNo.setText(rs.getString("ContactNo"));
                cb_Qualification.setSelectedItem(rs.getString("Qualification"));
                jDateChooser_DOJ.setDate(rs.getDate("DOJ"));
                ta_Address.setText(rs.getString("Address"));
                byte[] imageBytes = rs.getBytes("EmpImage");
                //ByteArrayInputStream bais = new ByteArrayInputStream(imageBytes);
                ImageIcon imageIcon = new ImageIcon(imageBytes, "Employee Image");
                label_Image.setIcon(imageIcon);
                employeeImage = imageBytes;

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        MySQLConnect mysql = new MySQLConnect();
        String sql = "update employeeinfo set Name=?, Gender=?, Age=?, BloodGroup=?, ContactNo=?, Qualification=?, DOJ=?, Address=?, EmpImage=? where EmployeeID=?";
        try (Connection conn = mysql.open(mysql.connectionString, mysql.userName, mysql.passWord);
                PreparedStatement stmt = conn.prepareStatement(sql);) {

            stmt.setString(1, tf_Name.getText());
            if (jRadioButton_Male.isSelected()) {
                stmt.setString(2, "Male");
            } else if (jRadioButton_Female.isSelected()) {
                stmt.setString(2, "Female");
            }

            stmt.setString(3, tf_Age.getText());
            stmt.setString(4, tf_BloodGroup.getText());
            stmt.setString(5, tf_ContactNo.getText());
            stmt.setString(6, (String) cb_Qualification.getSelectedItem());
            java.util.Date date = jDateChooser_DOJ.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String strDOJ = sdf.format(date);
            stmt.setString(7, strDOJ);
            stmt.setString(8, ta_Address.getText());
            stmt.setBytes(9, employeeImage);
            stmt.setString(10, tf_EmployeeID.getText());
            stmt.execute();
            JOptionPane.showMessageDialog(null, "Updated Succesfully");
            clearFields();
            updateTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (tf_EmployeeID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "PLEASE SELECT A RECORD TO DELETE");
        } else {
            int option = JOptionPane.showConfirmDialog(null, "DO YOU WANT TO DELETE EMPLOYEE" + tf_Name.getText(), "Delete Employee", JOptionPane.YES_NO_OPTION);

            if (option == 0) {
                MySQLConnect mysql = new MySQLConnect();
                String sql = "Delete from employeeinfo where EmployeeID=?";
                try (Connection conn = mysql.open(mysql.connectionString, mysql.userName, mysql.passWord);
                        PreparedStatement stmt = conn.prepareStatement(sql);) {

                    stmt.setString(1, tf_EmployeeID.getText());
                    stmt.execute();
                    JOptionPane.showMessageDialog(null, "Deleted Employee" + tf_Name.getText());
                    clearFields();
                    updateTable();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

