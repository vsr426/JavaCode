    private void btn_ImageUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ImageUploadActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Image Files", "jpg", "jpeg", "png"));
        int option = fileChooser.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            fileName = file.getAbsolutePath();
            txt_ImagePath.setText(fileName);
            File image = new File(fileName);
            try {
                BufferedImage buffImage = Thumbnails.of(image).size(220, 230).asBufferedImage();
                viewImage = new ImageIcon(buffImage);
                label_Image.setIcon(viewImage);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(buffImage, "jpg", baos);
                employeeImage = baos.toByteArray();
            } catch (IOException ex) {
                Logger.getLogger(EmployeeInfo.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btn_ImageUploadActionPerformed

